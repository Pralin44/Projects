function toggleSubMenus() {
    const subMenus = document.getElementById("subMenus");
    subMenus.style.display = subMenus.style.display === "block" ? "none" : "block";
}

function login() {
    alert("Login functionality will be implemented later.");
    const subMenus = document.getElementById("subMenus");
    subMenus.style.display = "none";
}

function logout() {
    alert("Logout functionality will be implemented later.");
    const subMenus = document.getElementById("subMenus");
    subMenus.style.display = "none";
}